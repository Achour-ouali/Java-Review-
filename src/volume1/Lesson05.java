package volume1;
//for(initialiization;condition;iteration)
public class Lesson05 {
    public static void main(String[] args){
        int counter,counter2,counter3,counter4;
//incremantal++
        for(counter=0;counter<=10;counter=counter+1)
            System.out.println("the counter is = " +counter);
        for(counter2=100;counter2>40;counter2=counter2-10)
            System.out.println("counter is equal " +counter2);
        for(counter4=0;counter4<=16;counter4=counter4+2)
            System.out.println("counter4 is " +counter4);

        //decremantel--
        for(counter3=10;counter3>0;counter3--)
            System.out.println(counter3 +"\n");





    }
}
