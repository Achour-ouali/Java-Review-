package volume02;

public class lesson10WhileLoop {
    public static void main(String[]args){
        //the difference between forloop and while loop the intialization
        //for loop the intialization we do it inside the loop but for while loop the inialisation we do it before the loop
        int a;
        a=1;

        while (a<=20){
            System.out.println("the value of a is "+a);
            a++; } }
}
